"""
Final-results aggregator:

1. Append BiLSTM row to results/results_summary.csv (appends only,
   does not modify existing rows).
2. Compute and print best seq_len/truncation config from
   results/seq_len_sweep_summary.csv.
3. Write results/FINAL_REPORT.md with a consolidated table and pointer
   notes to all artefact files.
4. Update memory file (the auto memory at ~/.claude/.../MEMORY.md is
   not touched here; this script only writes inside the repo).
"""
from __future__ import annotations

import csv
import json
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
RESULTS = REPO_ROOT / "results"
SUMMARY = RESULTS / "results_summary.csv"


def append_bilstm_row() -> dict | None:
    src = RESULTS / "bilstm_summary.json"
    if not src.exists():
        print(f"[WARN] {src} missing — BiLSTM row not appended")
        return None
    with open(src) as f:
        b = json.load(f)
    row = {
        "Method": "BiLSTM",
        "accuracy_avg": b["accuracy_mean"], "accuracy_std": b["accuracy_std"],
        "sensitivity_avg": b["sensitivity_mean"], "sensitivity_std": b["sensitivity_std"],
        "precision_avg": b["precision_mean"], "precision_std": b["precision_std"],
        "f_score_avg": b["f_score_mean"], "f_score_std": b["f_score_std"],
        "auc_avg": b["auc_mean"], "auc_std": b["auc_std"],
    }
    # Skip if already present
    existing = []
    with open(SUMMARY) as f:
        existing = list(csv.DictReader(f))
    if any(r.get("Method") == "BiLSTM" for r in existing):
        print("[INFO] BiLSTM row already in results_summary.csv")
        return row
    fieldnames = list(row.keys())
    with open(SUMMARY, "a", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writerow(row)
    print(f"[OK] appended BiLSTM to {SUMMARY}")
    return row


def best_seq_len() -> dict | None:
    sweep = RESULTS / "seq_len_sweep_summary.csv"
    if not sweep.exists():
        print(f"[WARN] {sweep} missing")
        return None
    with open(sweep) as f:
        rows = list(csv.DictReader(f))
    if not rows:
        return None
    rows.sort(key=lambda r: float(r["f_score_mean"]), reverse=True)
    best = rows[0]
    print(f"[OK] best seq_len config: max_seq_len={best['max_seq_len']} "
          f"truncation={best['truncation']} f1={best['f_score_mean']}")
    return best


def write_final_report(bilstm_row: dict | None, best_cfg: dict | None) -> None:
    out = RESULTS / "FINAL_REPORT.md"
    lines: list[str] = []
    lines.append("# GAME-Mal — Final Report")
    lines.append("")
    lines.append("Auto-generated by `scripts/aggregate_final_results.py`. "
                 "Numbers are 3-fold stratified CV (seed=42) unless noted.")
    lines.append("")
    lines.append("## Benchmark (results/results_summary.csv)")
    lines.append("")
    lines.append("| Method | Accuracy | Macro-F1 | AUROC |")
    lines.append("|---|---|---|---|")
    with open(SUMMARY) as f:
        for r in csv.DictReader(f):
            lines.append(
                f"| {r['Method']} "
                f"| {float(r['accuracy_avg']):.4f} ± {float(r['accuracy_std']):.4f} "
                f"| {float(r['f_score_avg']):.4f} ± {float(r['f_score_std']):.4f} "
                f"| {float(r['auc_avg']):.4f} ± {float(r['auc_std']):.4f} |"
            )
    lines.append("")

    lines.append("## Sequence-length / truncation sweep")
    lines.append("")
    sweep_csv = RESULTS / "seq_len_sweep_summary.csv"
    if sweep_csv.exists():
        lines.append("| max_seq_len | truncation | Macro-F1 | Accuracy | AUROC |")
        lines.append("|---|---|---|---|---|")
        with open(sweep_csv) as f:
            rows = list(csv.DictReader(f))
        rows.sort(key=lambda r: float(r["f_score_mean"]), reverse=True)
        for r in rows:
            lines.append(
                f"| {r['max_seq_len']} | {r['truncation']} "
                f"| {float(r['f_score_mean']):.4f} ± {float(r['f_score_std']):.4f} "
                f"| {float(r['accuracy_mean']):.4f} ± {float(r['accuracy_std']):.4f} "
                f"| {float(r['auc_mean']):.4f} ± {float(r['auc_std']):.4f} |"
            )
        if best_cfg:
            lines.append("")
            lines.append(f"**Best:** max_seq_len={best_cfg['max_seq_len']}, "
                         f"truncation={best_cfg['truncation']}, "
                         f"Macro-F1={float(best_cfg['f_score_mean']):.4f}.")
    else:
        lines.append("_seq_len sweep summary not found_")
    lines.append("")

    lines.append("## Deletion test (explainability faithfulness)")
    lines.append("")
    dt = RESULTS / "deletion_test.json"
    if dt.exists():
        with open(dt) as f:
            d = json.load(f)
        lines.append("Mean drop in true-class probability after masking the top-k "
                     "highest-gate tokens vs k random tokens. Larger "
                     "`Δgate − Δrandom` = more faithful gate-based explanation.")
        lines.append("")
        lines.append("| k | Δ gate-mask | Δ random-mask | gate − random |")
        lines.append("|---|---|---|---|")
        for k_key, s in d.get("overall", {}).items():
            k = k_key.split("_", 1)[1]
            lines.append(
                f"| {k} | {s['mean_delta_gate']:.4f} "
                f"| {s['mean_delta_random']:.4f} "
                f"| {s['delta_gate_minus_random']:.4f} |"
            )
    else:
        lines.append("_deletion_test.json not found_")
    lines.append("")

    lines.append("## API semantic groups")
    lines.append("")
    sg = RESULTS / "api_semantic_groups.json"
    if sg.exists():
        with open(sg) as f:
            g = json.load(f)
        lines.append("Heuristic bucket histograms of the gate-ranked top-15 APIs "
                     "per family. Buckets: " + ", ".join(g["buckets"]) + ".")
        lines.append("")
        lines.append("| Family | Histogram |")
        lines.append("|---|---|")
        for fam, body in g["by_family"].items():
            hist = ", ".join(f"{k}={v}" for k, v in body["bucket_histogram"].items())
            lines.append(f"| {fam} | {hist} |")
    else:
        lines.append("_api_semantic_groups.json not found_")
    lines.append("")

    lines.append("## Artefacts")
    lines.append("")
    for p in sorted(RESULTS.glob("*.csv")):
        lines.append(f"- `results/{p.name}`")
    for p in sorted(RESULTS.glob("*.json")):
        lines.append(f"- `results/{p.name}`")
    out.write_text("\n".join(lines) + "\n")
    print(f"[OK] wrote {out}")


def main() -> None:
    bilstm_row = append_bilstm_row()
    best_cfg = best_seq_len()
    write_final_report(bilstm_row, best_cfg)


if __name__ == "__main__":
    main()
